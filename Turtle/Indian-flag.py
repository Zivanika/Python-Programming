import turtle
t=turtle.Turtle()
t.speed(3)
def rectangle(x,color):
    t.goto(-100,x)
    t.begin_fill()
    t.fillcolor(color)
    for i in range(2):
        t.forward(350)
        t.right(90)
        t.forward(75)
        t.right(90)
    t.end_fill()
t.up()
t.goto(-100,-300)
t.down()
t.pensize(2)
rectangle(300,"#FF9933")
# rectangle(225,"white")
t.goto(-100,225)
t.forward(175)
t.color("#000088")
t.circle(-37)
t.setheading(270)
t.forward(37)
t.setheading(90)
for i in range(24):
    t.forward(37)
    t.back(37)
    t.right(20)
t.color("black")
t.up()
t.goto(75,225)
t.down()
t.setheading(0)
t.forward(175)
t.right(90)
t.forward(75)
t.right(90)
t.forward(300)
t.setheading(0)
rectangle(150,"#128807")


turtle.done()